import sys
import platform

def os_is_mac():
    return platform.system() in ('Darwin', 'darwin')

def os_is_windows():
    return sys.platform == 'win32'

def get_library_search_path_env():
    if os_is_windows():
        return "PATH"
    else:
        return "LD_LIBRARY_PATH"
